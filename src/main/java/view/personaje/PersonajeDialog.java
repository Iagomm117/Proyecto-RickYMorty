    public void setIconImageLabel(String s) throws IOException{
        URL url = new URL(s);
        Image image = ImageIO.read(url);
        imageLabel.setIcon(new ImageIcon(image));
        int alto = 200;
        int ancho = 200;
        Image imagenRedimensionada = image.getScaledInstance(alto, ancho, Image.SCALE_SMOOTH);
        imageLabel.setIcon(new ImageIcon(imagenRedimensionada));
    }
    
